package zz.csg.api.constraints;

import java.awt.geom.Point2D;

import zz.utils.properties.IRWProperty;

public interface IConstrainedPoint extends IRWProperty<Point2D>
{
	/**
	 * Shortcut for the set method.
	 */
	public void set(double aX, double aY);
	
	/**
	 * Returns a variable that represents the X coordinate of this point
	 */
	public IConstrainedDouble pX();

	/**
	 * Returns a variable that represents the Y coordinate of this point
	 */
	public IConstrainedDouble pY();

}