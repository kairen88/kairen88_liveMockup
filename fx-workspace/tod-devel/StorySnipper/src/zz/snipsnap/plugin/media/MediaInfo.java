package zz.snipsnap.plugin.media;

import java.awt.Dimension;
import java.util.Date;

/**
 * Encapsulates metadata about a media object
 * @author gpothier
 */
public class MediaInfo
{
    public String itsName;
    
	/**
	 * Size of the media in bytes.
	 */
	public long itsSize;
	
	/**
	 * Resolution of the media in pixels, if available.
	 */
	public Dimension itsResolution;
	
	/**
	 * Duration of the media in seconds, if available.
	 */
	public Double itsDuration;
	
	/**
	 * Date the media was produced.
	 */
	public Date itsDate;

	public Date getDate()
	{
		return itsDate;
	}

	public void setDate(Date aDate)
	{
		itsDate = aDate;
	}

	public Double getDuration()
	{
		return itsDuration;
	}

	public void setDuration(Double aDuration)
	{
		itsDuration = aDuration;
	}

	public Dimension getResolution()
	{
		return itsResolution;
	}

	public void setResolution(Dimension aResolution)
	{
		itsResolution = aResolution;
	}

	public long getSize()
	{
		return itsSize;
	}

	public void setSize(long aSize)
	{
		itsSize = aSize;
	}

	public String getName()
	{
		return itsName;
	}

	public void setName(String aName)
	{
		itsName = aName;
	}
	
	
}