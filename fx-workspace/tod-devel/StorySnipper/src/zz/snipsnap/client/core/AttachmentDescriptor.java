package zz.snipsnap.client.core;

import java.util.Date;

/**
 * Describes a given snip attachment.
 * @author gpothier
 */
public class AttachmentDescriptor
{
	private String itsName;
	private String itsContentType;
	private long itsSize;
	private Date itsModificationDate;
	private String itsLocation;
	
	public AttachmentDescriptor()
	{
	}

	public AttachmentDescriptor(String aName, String aContentType, long aSize, Date aModificationDate, String aLocation)
	{
		itsName = aName;
		itsContentType = aContentType;
		itsSize = aSize;
		itsModificationDate = aModificationDate;
		itsLocation = aLocation;
	}

	public String getContentType()
	{
		return itsContentType;
	}
	
	public void setContentType(String aContentType)
	{
		itsContentType = aContentType;
	}
	
	public String getLocation()
	{
		return itsLocation;
	}
	
	public void setLocation(String aLocation)
	{
		itsLocation = aLocation;
	}
	
	public Date getModificationDate()
	{
		return itsModificationDate;
	}
	
	public void setModificationDate(Date aModificationDate)
	{
		itsModificationDate = aModificationDate;
	}
	
	public String getName()
	{
		return itsName;
	}
	
	public void setName(String aName)
	{
		itsName = aName;
	}
	
	public long getSize()
	{
		return itsSize;
	}
	
	public void setSize(long aSize)
	{
		itsSize = aSize;
	}
	
	
	
}
