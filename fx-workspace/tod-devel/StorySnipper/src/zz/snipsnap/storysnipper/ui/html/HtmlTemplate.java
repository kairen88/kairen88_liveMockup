package zz.snipsnap.storysnipper.ui.html;

import javax.swing.text.html.HTML;
import javax.swing.text.html.HTML.Tag;

/**
 * An Html template that can be inserted in the Html document.
 * See {@link zz.snipsnap.storysnipper.ui.html.HtmlLayoutComponent#insertTemplate(HtmlTemplate)}
 * @author gpothier
 */
public class HtmlTemplate
{
	private HTML.Tag itsTag;
	private String itsHtmlFragment;
	
	public HtmlTemplate(Tag aTag, String aHtmlFragment)
	{
		itsTag = aTag;
		itsHtmlFragment = aHtmlFragment;
	}

	public String getHtmlFragment()
	{
		return itsHtmlFragment;
	}
	

	public HTML.Tag getTag()
	{
		return itsTag;
	}
	
}
