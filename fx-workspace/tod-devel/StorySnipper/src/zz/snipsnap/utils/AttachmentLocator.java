package zz.snipsnap.utils;

/**
 * Contains the information that permits to locate an attachment within a space. 
 * @author gpothier
 */
public class AttachmentLocator
{
	private String itsSnipName;
	private String itsFileName;
	
	public AttachmentLocator(String aSnipName, String aFileName)
	{
		itsSnipName = aSnipName;
		itsFileName = aFileName;
	}

	public String getFileName()
	{
		return itsFileName;
	}

	public String getSnipName()
	{
		return itsSnipName;
	}
}
