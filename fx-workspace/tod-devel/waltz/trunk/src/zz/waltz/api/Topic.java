package zz.waltz.api;

/**
 * Topic for data published by {@link zz.waltz.api.WaltzComponent}
 * @author gpothier
 */
public class Topic
{
	private String itsName;

	public Topic()
	{
	}

	public Topic(String aName)
	{
		itsName = aName;
	}
	
	public String getName()
	{
		return itsName;
	}
	
	public String toString()
	{
		return getName();
	}
}
